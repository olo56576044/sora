package kr.or.dw.store.vo;

public class ImgStoreVO {
	private int img_no;
	private int store_no;
	private String img_url;
	private String img_updt;
	private String gb_rep;
	
	public int getImg_no() {
		return img_no;
	}
	public void setImg_no(int img_no) {
		this.img_no = img_no;
	}
	public int getStore_no() {
		return store_no;
	}
	public void setStore_no(int store_no) {
		this.store_no = store_no;
	}
	public String getImg_url() {
		return img_url;
	}
	public void setImg_url(String img_url) {
		this.img_url = img_url;
	}
	public String getImg_updt() {
		return img_updt;
	}
	public void setImg_updt(String img_updt) {
		this.img_updt = img_updt;
	}
	public String getGb_rep() {
		return gb_rep;
	}
	public void setGb_rep(String gb_rep) {
		this.gb_rep = gb_rep;
	}
	
	
}
